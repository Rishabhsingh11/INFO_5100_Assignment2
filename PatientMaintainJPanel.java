    private void populateTable() 
    {
       DefaultTableModel model = (DefaultTableModel)tblPatient.getModel();
       model.setRowCount(0);
       
       for(Patient patient : patientdirectory.getPatientDirectory())
       {
           if(String.valueOf(patient.getHospital_id()).equals(txtHospitalId.getText()))
           {
           Object [] row = new Object[12];
           row[0] = patient;
           row[1] = patient.getAge();
           row[2] = patient.getGender();
           row[3] = patient.getPatient_id();
           row[4] = patient.getCity();
           row[5] = patient.getState();
           row[6] = patient.getCommunity();
           row[7] = patient.getMobile();
           row[8] = patient.getEmail();
           row[9] = patient.getHospital_id();
           row[10] = patient.getHospital_name();
           row[11] = patient.getPatient_password();
           model.addRow(row);
           }
       }
    }
