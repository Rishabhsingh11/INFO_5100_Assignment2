    private void btnFetchPersonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFetchPersonActionPerformed
        // TODO add your handling code here:
        boolean flag = false;
        for(Person per : persondirectory.getPersonDirectory())
        {
            if ((String.valueOf(per.getSSN_id()).equals(txtSsnId.getText())))
            {
                txtName.setText(per.getName());
                txtAge.setText(String.valueOf(per.getAge()));
                txtGender.setText(per.getGender());
                txtCity.setText(per.getCity());
                txtState.setText(per.getState());
                txtCommunity.setText(per.getCommunity());
                txtMobile.setText(String.valueOf((per.getMobile())));
                txtEmail.setText(per.getEmail());

                flag = false;

                txtSsnId.setEnabled(false);
                txtName.setEnabled(false);
                txtGender.setEnabled(false);
                txtAge.setEnabled(false);
                txtCity.setEnabled(false);
                txtCommunity.setEnabled(false);
                txtState.setEnabled(false);
                txtMobile.setEnabled(false);
                txtEmail.setEnabled(false);
                break;
            }
            else
            {
                flag = true;
            }
        }
        if (flag == true)
        {
            JOptionPane.showMessageDialog(this, "User doesn't exits");
        }
    }//GEN-LAST:event_btnFetchPersonActionPerformed

